var a = prompt("Введите единицу измерения:\n1. Дециметр\n2. Километр\n3. Метр\n4. Миллиметр\n5. Сантиметр");
var b = prompt("Введите длину отрезка \"L\"");
a = parseInt(a);
var x;
if (a === 1) x = b / 10;
if (a === 2) x = b * 1000;
if (a === 3) x = b;
if (a === 4) x = b / 1000;
if (a === 5) x = b / 100;
alert("Отрезок равен " + x + " м");
